            Nino Nakano (Jepang: 中野なかの 二乃にの, Nakano Nino) adalah seorang karakter fiksi dan salah satu tokoh protagonis wanita utama dari seri manga dan anime The Quintessential Quintuplets yang diciptakan oleh Negi Haruba. Dalam cerita, dia merupakan putri kedua dari kembar lima Nakano. Ia biasanya bertindak sebagai sosok ibu di keluarganya, dan melakukan hal seperti memasak dan merawat kesehatan saudari-saudarinya.[vol. 1, 3][ch. 26]

            Di antara kembar lima, dia adalah orang yang paling membenci Fūtarō dan sering memberontak melawannya pada awal seri. Seiring cerita berlangsung, rasa bencinya perlahan mulai memudar setelah terjadinya beberapa peristiwa yang mengubah sifatnya terhadap para saudarinya, dan juga kepada Fūtarō—yang berkembang menjadi rasa suka. Meski pada awalnya dia menolak menerima perasaannya yang baru,[ch. 50, 54] Nino kemudian menjadi orang pertama yang mengakui perasaannya kepada Fūtarō.[ch. 59, 60]

            Penciptaan dan konsepsi
            Nino adalah gadis yang tampak dingin dengan rambut lurus panjang. Ia memiliki rambut merah muda kemerahan, dan rambutnya digambarkan dengan warna yang berbeda-beda—mulai dari merah muda hingga telemagenta,[vol. 1] dan merah muda antik hingga campuran merah dan ungu pada volume 4.[ch. 33] Aksesorisnya yang terkenal adalah sepasang pita berbentuk kupu-kupu di kedua sisi kepalanya.[ch. 1] Sama seperti para saudari kembarnya, dia memiliki mata biru gelap, tinggi badan yang rata-rata, dan tubuh yang proporsional. Nino mengenakan sweater lengan panjang yang menutupi seragamnya. Ia menggunakan lensa kontak karena penglihatannya yang buruk.[ch. 5] Serupa dengan Ichika, dia juga menggunakan tas tangan.[ch. 1] Nino tidak menggunakan anting, tetapi ia berencana untuk menggunakannya, setidaknya sebelum menikah.[ch. 14]</div>
