            <h2 class="sisi-2">Belum Selesai, Attack on Titan Akan Rilis Chapter Baru pada 2024!</h2><br><br>
            <P class="sisi-3">Attack on Titan menjadi salah satu anime yang populer dalam satu dekade ke belakang ini. Anime yang diadaptasi dari manga karya Hajime Isayama ini pun memiliki penggemar di hampir seluruh negara, tak terkecuali Indonesia. Setelah melewati perjalanan yang cukup panjang, anime ini pun akhirnya menamatkan jalan ceritanya pada awal November 2023 lalu.

            Yap, kisah Eren serta kawan-kawannya bisa dibilang sudah benar-benar berakhir lewat Attack on Titan: The Final Chapters Special 2 yang rilis pada 4 November lalu. Hal ini pun berlangsung sekitar 2,5 tahun setelah manga-nya lebih awal tamat pada 2021 lalu lewat Volume 34. Meski begitu, belum lama ini beredar kabar bahwa cerita Attack on Titan akan memiliki chapter baru pada 2024 mendatang.

            Chapter baru manga Attack on Titan
            Melansir Kotaku, Hajime Isayama secara langsung mengonfirmasi bahwa ia sedang menggarap Attack on Titan Vol. 35 yang bertajuk “Attack on Titan Fly.” Volume terbaru ini sebenarnya adalah art book yang berisi ilustrasi Isayama selama proses pembuatan manga AoT. Namun, sebagai bonus, volume 35 ini juga akan memiliki chapter tambahan yang berkaitan dengan cerita utama AoT.

            “Saat ini saya sedang menulis manga baru untuk art book ini. Ini adalah manga bonus yang termasuk dalam Attack on Titan Volume 35. Saya harap Anda menantikannya,” ungkap Isayama. Sang kreator memang enggak mengungkapkan tentang siapa cerita pada chapter terbaru tersebut. Namun, belum lama ini beredar bocoran di Twitter bahwa cerita pada chapter tersebut akan berfokus pada masa kecil Levi Ackerman.</P>
